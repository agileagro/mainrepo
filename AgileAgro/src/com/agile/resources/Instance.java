package com.agile.resources;

public class Instance {
	
	public String instance_id;
	public String instance_name;
	public String country;
	public String org;
	public long total_area;
	public long covarage;
	public int sector_count;
	public String location;
	public String def_user;
	public String def_pass;
	public int current_stat;
	
	public String getInstance_id() {
		return instance_id;
	}
	public void setInstance_id(String instance_id) {
		this.instance_id = instance_id;
	}
	public String getInstance_name() {
		return instance_name;
	}
	public void setInstance_name(String instance_name) {
		this.instance_name = instance_name;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getOrg() {
		return org;
	}
	public void setOrg(String org) {
		this.org = org;
	}
	public long getTotal_area() {
		return total_area;
	}
	public void setTotal_area(long total_area) {
		this.total_area = total_area;
	}
	public long getCovarage() {
		return covarage;
	}
	public void setCovarage(long covarage) {
		this.covarage = covarage;
	}
	public int getSector_count() {
		return sector_count;
	}
	public void setSector_count(int sector_count) {
		this.sector_count = sector_count;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getDef_user() {
		return def_user;
	}
	public void setDef_user(String def_user) {
		this.def_user = def_user;
	}
	public String getDef_pass() {
		return def_pass;
	}
	public void setDef_pass(String def_pass) {
		this.def_pass = def_pass;
	}
	public int getCurrent_stat() {
		return current_stat;
	}
	public void setCurrent_stat(int current_stat) {
		this.current_stat = current_stat;
	}
	
	

}
